                    Khalifa International Stadium is a multi-purpose stadium located in Al Rayyan, Qatar, around 9 kilometers (6 miles) west from the center of Doha. Its ground comprises a running track and a grass pitch. Opened in 1976, the stadium was named after then-Emir of Qatar Khalifa bin Hamad Al Thani, and under the ownership of the Qatar Football Association, it serves as the primary home ground of the Qatar men's national football team. Its current fully-roofed, 45,857-seat configuration was opened in 2017, following a previous reconfiguration in 2005 that incorporated the stadium into the Aspire Zone complex and added a roofed grandstand; boosting its capacity from 20,000 to 40,000.
